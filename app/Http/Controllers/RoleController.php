    public function all_roles(Request $request)
    {
        if ($request->user()) {
            if ($request->user()->authorizeRoles(['admin'])) {
                $roles = DB::table('roles')->get()->all();
                return view('roles_list', compact('roles'));
            }
        }
        return abort(401, 'Unauthorized request');
    }

